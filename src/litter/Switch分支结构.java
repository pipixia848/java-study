package litter;

public class Switch分支结构 {
    public static void main(String[] args) {

        int a=2;

        switch(a) {
            case 1:
                System.out.println("bupipei");
                break;
            case 2:
                System.out.println("pipei");
                break;
            default:
                System.out.println("cuowu");
                break;

        }
    }
}






















